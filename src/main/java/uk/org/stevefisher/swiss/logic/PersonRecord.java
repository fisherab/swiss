package uk.org.stevefisher.swiss.logic;

public class PersonRecord {

	private int wins;
	private String name;

	public PersonRecord(String name) {
		setName(name);
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getWins() {
		return wins;
	}

	public void setWins(int wins) {
		this.wins = wins;
	}

}
